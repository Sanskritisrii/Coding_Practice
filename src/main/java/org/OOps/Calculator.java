package org.OOps;

public class Calculator {

    public int addition(int a,int b){
        return a+b;
    }
    public int addition(int a , int b, int c){
        return a+b+c;
    }
    public int multiplication(int a, int b){
        return a*b;
    }
    public int multiplication(int a, int b, int c){
        return a*b*c;
    }
}
